/**
 * Variable are containers for storing data. We can use var, let and const keywords to declare variable in JavaScript.
 */

{
    var x = 10;
    console.log(x);

    var x = 20;
    console.log(x);
    // Re-declaration allowed

    x = 30;
    console.log(x);
    // Re-assignation allowed
}

